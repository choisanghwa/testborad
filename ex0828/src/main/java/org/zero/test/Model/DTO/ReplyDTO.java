package org.zero.test.Model.DTO;

import java.util.Date;

public class ReplyDTO {
	private Integer rno;
	private Integer bno;
	private String replaytext;
	private String replay;
	private Date regdate;
	private String reid;
	private String replaypw;
	
	
	public Integer getRno() {
		return rno;
	}
	public void setRno(Integer rno) {
		this.rno = rno;
	}
	public Integer getBno() {
		return bno;
	}
	public void setBno(Integer bno) {
		this.bno = bno;
	}
	public String getReplaytext() {
		return replaytext;
	}
	public void setReplaytext(String replaytext) {
		this.replaytext = replaytext;
	}
	public String getReplay() {
		return replay;
	}
	public void setReplay(String replay) {
		this.replay = replay;
	}
	public Date getRegdate() {
		return regdate;
	}
	public void setRegdate(Date regdate) {
		this.regdate = regdate;
	}
	public String getReid() {
		return reid;
	}
	public void setReid(String reid) {
		this.reid = reid;
	}
	public String getReplaypw() {
		return replaypw;
	}
	public void setReplaypw(String replaypw) {
		this.replaypw = replaypw;
	}
	
	
	
	
}
